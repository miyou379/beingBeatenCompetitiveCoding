i64 qpow(i64 x, i64 y, i64 mod) {
  i64 ans = 1;
  while (y) {
    if (y & 1) ans = ans * x % mod;
    x = x * x % mod;
    y >>= 1;
  }
  return ans;
}


void Solution() {
  int l, r, k;
  std::cin >> l >> r >> k;
  if (k > 9) { std::cout << 0 << endl; return; }
  int sup = 9 / k;
  int res = qpow(sup+1, r, M1) - qpow(sup+1, l, M1);
  res = (res + M1) % M1;
  std::cout << res << endl;
}

